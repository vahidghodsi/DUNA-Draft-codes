import Head from "next/head";
import { useRouter } from "next/router";
import { useEffect } from "react";
import { useSelector, useDispatch } from "react-redux";
import { getEvaluation } from "@redux/actions/appData";
import EvaluationPanel from "@contents/evaluation-panel/evaluation-panel";

export default function EvaluationPanelId() {
  const router = useRouter();
  const evaluationId = router.query.evaluationId;
  const dispatch = useDispatch();
  const appUser = useSelector(state => state.appUser);
  const { evaluation } = useSelector(state => state.appData);
  // console.log("[project-Panel/]: ", project);

  useEffect(() => {
    if (appUser && appUser.id) {
      dispatch(getEvaluation(evaluationId));
    }
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [evaluationId, appUser]);

  return (
    <>
      <Head>
        <title>DUNA - Member Area - {evaluation?.title || "Project"} </title>
        <meta name="description" content="Generated by create next app" />
      </Head>
      <EvaluationPanel evaluation={evaluation} />
    </>
  );
}
